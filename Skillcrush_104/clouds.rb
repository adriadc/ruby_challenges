weather = 'rain'
case weather
	when 'sun'
		puts "Awesome, sunglasses today"
	when 'rain'
		puts "Oh no, put on boots!"
	when 'clouds'
		puts "It is all good. wear what you want. watch out for rain!"
	when 'snow'
		puts "Cold! Boots, gloves, hat, coat!"
	else
		puts "I am not sure! Look outside!"
	end